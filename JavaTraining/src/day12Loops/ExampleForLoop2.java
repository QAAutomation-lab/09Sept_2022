package day12Loops;

public class ExampleForLoop2 {

	public static void main(String[] args) {
		
		/**
		 * for(initialization; condition; incre/decre)
		 * {
		 * 
		 * } 
		 * 
		 * execution flow:
		 * 				1. initialization
		 * 				2. condition
		 * 				3. executes body
		 * 				4. incre/decre
		 * 				5. repeat from step 2 till condition gets failed
		 */
		for(int i=0;i<15;i++)
		{
			System.out.println("Hello, Good Morning Everyone...");
		}
	}

}
